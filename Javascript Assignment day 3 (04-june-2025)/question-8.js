// Count to 20 with all the loops

// Using for loop

for (let i = 1; i <= 20; i++) {
  console.log("The number is", i);
}

// using do while loop

let i = 1;
do {
  console.log("The number is", i);
  i++;
} while (i <= 20);

// while loop

let j = 1;
while (j <= 20) {
  console.log("The number is", j);
  j++;
}
